export const formatDate = (isoDate: string) => {
  const date = new Date(isoDate);
  const day = String(date.getDate()).padStart(2, "0");
  const month = String(date.getMonth() + 1).padStart(2, "0"); // Months are zero-based in JS
  const year = date.getFullYear();
  const hours = String(date.getHours()).padStart(2, "0");
  const minutes = String(date.getMinutes()).padStart(2, "0");

  return `${day}/${month}/${year} (${hours}:${minutes})`;
};

export const getTotals = (items: FoodItem[]) => {
  return items.reduce(
    (totals, item) => {
      totals.carbs_g += item.carbs_g;
      totals.protein_g += item.protein_g;
      totals.fat_g += item.fat_g;
      totals.calories += item.calories;
      return totals;
    },
    { carbs_g: 0, protein_g: 0, fat_g: 0, calories: 0 }
  );
}
